package com.kn.arraytasks;

public class ForwardArrayTraversing {
	
	//forward array traversal behavior
	public void ArrayElemntsForwardTraversal(int[] arr) {
		System.out.println("************************************");
		System.out.println("Elements in the array are: ");
		for(int i=0;i<arr.length;i++) {
			System.out.print(arr[i]+" ");
		}
	}

}
